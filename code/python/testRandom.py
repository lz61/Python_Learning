# 2: 比例: 0.8 4的比例: 0.2
import random
# 函数是什么? y=f(x)
# 编程语言的复杂性: 放进去一个x,经过函数之后出来y
# 可以放进去的变量可以是多个,出来的变量也可以是多个
# random()
# 函数格式: 函数名()
x = random.random() # 输入: 什么都没有 输出: 0-1 中间随机的一个数
num = 0
# 控制判断: if 和 else
if x<0.8:
    num = 2
else:
    num = 4
print(num)


